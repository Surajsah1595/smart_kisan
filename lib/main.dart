  /// Purpose: Asynchronously loads the user's last selected UI theme from disk.
  /// Inputs: None.
  /// Outputs: Updates [_themeMode] and triggers a rebuild.
  Future<void> _loadThemePreference() async {
    try {
      // 1. Open the local SharedPreferences instance.
      final prefs = await SharedPreferences.getInstance();
      // 2. Fetch the stored string, defaulting to 'Auto Mode' if none exists.
      final theme = prefs.getString('ui_theme') ?? 'Auto Mode';
      print(' Loading theme from preferences: $theme');
      
      // 3. Convert the string to a native Flutter ThemeMode enum and update state.
      setState(() {
        _themeMode = _stringToThemeMode(theme);
      });
      print(' Theme loaded: $_themeMode');
    } catch (e) {
      // 4. Log any file system errors without crashing.
      print(' Error loading theme: $e');
    }
  }

  /// Purpose: Sets up the authentication listener. If a user is logged in, it attaches to their specific Firestore notification stream.
  /// Inputs: None.
  /// Outputs: Modifies the [_authSub] subscription.
  void _initializeNotifications() {
    try {
      // 1. Grab the singleton FirebaseAuth instance.
      final auth = FirebaseAuth.instance;

      // 2. Immediate check: If the app cold-boots and the user is already logged in, attach the stream immediately.
      if (auth.currentUser != null) {
        _attachNotificationListener();
      }

      // 3. Reactive check: Listen for future sign-ins or sign-outs during the app's lifecycle.
      _authSub = auth.authStateChanges().listen((user) {
        if (user != null) {
          // 4. User logged in: safe to query their private Firestore collections.
          _attachNotificationListener();
        } else {
          // 5. User signed out: cancel the active snapshot listener to prevent permission errors and memory leaks.
          _notifSub?.cancel();
          _notifSub = null;
        }
      });

      print(' Notifications initialization (auth watcher) set up');
    } catch (e) {
      print(' Error initializing notification auth watcher: $e');
    }
  }

  /// Purpose: Listens to the `notifications` Firestore collection and triggers local UI snackbars for high-priority alerts.
  /// Inputs: None. (Relies on the authenticated UID).
  /// Outputs: Modifies the [_notifSub] subscription.
  void _attachNotificationListener() {
    try {
      // 1. Guard clause: Ensure we don't accidentally create multiple overlapping stream listeners.
      if (_notifSub != null) return;

      final service = NotificationService();
      // 2. Fetch the current user ID securely.
      final uid = FirebaseAuth.instance.currentUser?.uid ?? '';
      
      // 3. Subscribe to the real-time Firebase stream.
      _notifSub = service.getNotificationsStream().listen((snapshot) {
        // 4. Filter the incoming documents strictly to the logged-in user as an extra security/data-integrity measure.
        final docs = uid.isNotEmpty
            ? snapshot.docs.where((d) {
                final data = d.data() as Map<String, dynamic>?;
                return data != null && (data['userId'] == uid || data['uid'] == uid);
              }).toList()
            : snapshot.docs;
            
        if (docs.isNotEmpty) {
          // 5. Extract the most recent notification payload from the top of the sorted stream.
          final latestData = docs.first.data() as Map<String, dynamic>?;
          if (latestData != null) {
            final title = latestData['title'] ?? 'Notification';
            final priority = (latestData['priority'] ?? 'normal').toString().toLowerCase();
            final isRead = latestData['isRead'] ?? false;
            
            // 6. Logic gate: Only trigger the invasive UI snackbar if the alert is both Unread AND High-Priority.
            if (!isRead && priority == 'high') {
              print(' New high-priority notification: $title');
              // 7. Delay the snackbar render until after the current layout frame completes to avoid "build during build" exceptions.
              WidgetsBinding.instance.addPostFrameCallback((_) => _showNotificationSnackbar(title));
            }
          }
        }
      }, onError: (error) {
        print(' Notification stream error: $error');
      });
    } catch (e) {
      print(' Failed to attach notification listener: $e');
    }
  }

  /// Purpose: Renders a global green banner at the bottom of the screen.
  /// Inputs: [title] - The alert message text.
  /// Outputs: Mutates the global ScaffoldMessenger UI overlay.
  void _showNotificationSnackbar(String title) {
    // 1. Access the global context via the statically held navigatorKey.
    final context = navigatorKey.currentContext;
    if (context != null) {
      // 2. Push the UI snackbar into the view hierarchy.
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Text(' $title'),
          backgroundColor: Colors.green.shade700,
          duration: const Duration(seconds: 4),
        ),
      );
    }
  }

  /// Purpose: Public API to change the app's visual theme dynamically.
  /// Inputs: [theme] - A human readable string ('Light Mode', 'Dark Mode').
  /// Outputs: Rebuilds the root widget and saves to SharedPreferences.
  void setTheme(String theme) {
    // 1. Update the state variable immediately to trigger UI rebuild.
    setState(() {
      _themeMode = _stringToThemeMode(theme);
    });
    // 2. Asynchronously commit the new preference to disk.
    SharedPreferences.getInstance().then((prefs) => prefs.setString('ui_theme', theme));
  }

  /// Purpose: Helper function to map UI strings to Flutter enums.
  /// Inputs: [theme] - String selection from Settings UI.
  /// Outputs: Returns the native [ThemeMode] enum.
  ThemeMode _stringToThemeMode(String theme) {
    switch (theme) {
      case 'Light Mode':
        return ThemeMode.light;
      case 'Dark Mode':
        return ThemeMode.dark;
      case 'Auto Mode':
      default:
        // 1. Fallback to reading the host OS's system preferences.
        return ThemeMode.system;
    }
  }

