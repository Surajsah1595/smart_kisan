/// Purpose: The entry point of the Smart Kisan application. Initializes Firebase, local notifications, and global localization.
/// Inputs: Standard Flutter environment inputs.
/// Outputs: Bootstraps and runs the [MyApp] root widget.
Future<void> main() async {
  // 1. Ensure the Flutter engine is fully bound to the native platform before calling any async plugin code.
  WidgetsFlutterBinding.ensureInitialized();

  try {
    // 2. Initialize the Firebase SDK, using platform-specific settings generated by flutterfire.
    await Firebase.initializeApp(
      options: DefaultFirebaseOptions.currentPlatform,
    );
    // 3. Instantiate the global NotificationService to handle push alerts.
    final notificationService = NotificationService();
    // 4. Request device permissions and configure local notification channels.
    await notificationService.initFCM();
  } catch (e) {
    // 5. Catch initialization errors gracefully so the app can still boot (e.g. offline mode).
    print(' Firebase initialization warning: $e');
  }

  // 6. Retrieve the user's previously saved language preference from SharedPreferences.
  await LocalizationService.loadLanguage();
  // 7. Initialize the easy_localization engine to prepare translation assets.
  await EasyLocalization.ensureInitialized();

  // 8. Inflate the root widget, wrapping it in the localization provider.
  runApp(
    EasyLocalization(
      supportedLocales: const [Locale('en'), Locale('hi'), Locale('ne')],
      path: 'assets/translations',
      fallbackLocale: const Locale('en'),
      child: const MyApp(),
    ),
  );
}

